#include "header.h"

void	ft_putchar2(char c)
{
	write(2, &c, 1);
}

void	ft_puterror(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		ft_putchar2(str[a]);
		a++;
	}
}
